package com.used.example.domain;

public class MonthSum {
	
	private int month;
	private long total;
	@Override
	public String toString() {
		return "Sum [month=" + month + ", total=" + total + "]";
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public long getTotal() {
		return total;
	}
	public void setTotal(long total) {
		this.total = total;
	}
	
	
	
	
	
	
	

}
